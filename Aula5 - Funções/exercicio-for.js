let i=1;

while (i <= 10){
    console.log(i);
    i++;
}

console.log("------------");
// como fazer um for exatamente equivalente a esse while. 
// a estrutura do for é semelhante ao while. 

// inicialização condição e atualização para for e while
// esse let i é exclusivo do for
for (let i = 1; i <= 10; i++){
    console.log(i);    
}